dia_da_semana = int(input("Digite o dia da semana: "))

if dia_da_semana == 1:
    print("Domingo")
elif dia_da_semana == 2:
    print("Segunda-Feira")
elif dia_da_semana == 3:
    print("Terça-Feira")
elif dia_da_semana == 4:
    print("Quarta-Feira")
elif dia_da_semana == 5:
    print("Quinta-Feira")
elif dia_da_semana == 6:
    print("Sexta-Feira")
elif dia_da_semana == 7:
    print("Sábado")
else:
    print("Dia inválido")
