/**
 * Na maioria das linguagens de programação podemos trabalhar com o loop For
 * utilizando uma variável contadora que geralmente chamamos de i.
 */

// A variável contadora é incrementada a cada vez que o laço repete
for (let i = 0; i < 10; i++) {
    console.log(`O laço está repetindo pela ${i + 1} vez`)
}

